
// doubles the input number
function double(n) {
    return n * 2;
}

// squares the input number
function square(n) {
  return n * n;
}

// checks if the input number is even
function isEven(n) {
  return n % 2 === 0;
}

// checks if the input number is odd
function isOdd(n) {
  return n % 2 != 0;
}